// Import mongoose module
const mongoose = require('mongoose');

// Define user schema
const userSchema = new mongoose.Schema({
     userId:{
        type: String,
        required: true,
        unique: true,
    },
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phoneNumber: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    confirmPassword: {
        type: String,
        required: true
    }
});

// Create User model
const User = mongoose.model('customers', userSchema);

// Export User model
module.exports = User;
